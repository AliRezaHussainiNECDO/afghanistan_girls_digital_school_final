-- چرا ستون جدید به‌جای رمزگذاری درجا (in-place)؟ چون این یک اپ Production با
-- کاربران واقعی است: اگر همین حالا phone/date_of_birth را با متن رمزنگاری‌شده
-- جایگزین کنیم، هر سطر قدیمی که هنوز رمزنگاری نشده (چون کلید رمزنگاری تازه
-- تنظیم می‌شود) نامعتبر/غیرقابل‌خواندن می‌شود. به‌جایش:
--   ۱) این Migration فقط دو ستون جدید (خالی) اضافه می‌کند — کاملاً بی‌خطر.
--   ۲) کد سرور از این پس تلفن/تاریخ تولدِ کاربران *جدید* را رمزنگاری‌شده در
--      ستون‌های تازه ذخیره می‌کند (`lib/columnCrypto.ts`).
--   ۳) یک Endpoint یک‌باره برای ادمین (`POST /admin/security/backfill-encryption`)
--      سطرهای قدیمیِ متن‌ساده را رمزنگاری کرده و در ستون‌های تازه می‌نویسد.
--   ۴) وقتی Backfill کامل شد و تأیید شد (یک Migration بعدی، جداگانه)، ستون‌های
--      قدیمی metن‌سادهٔ `phone`/`date_of_birth` می‌توانند خالی/حذف شوند.
-- اجرا:
--   wrangler d1 migrations apply afghan_girls_school_db --remote
-- سپس (فقط یک‌بار، برای رمزنگاریِ کلید محرمانه):
--   openssl rand -base64 32 | wrangler secret put COLUMN_ENCRYPTION_KEY
-- ═══════════════════════════════════════════════════════════════════════════

ALTER TABLE users ADD COLUMN phone_enc TEXT;
